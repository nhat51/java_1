package codesnippet;

import com.sun.security.jgss.GSSUtil;

public class TestconditionalOperators {
    public static void main(String[] args) {
        int value1 = 10;
        int value2 = 20;

        System.out.println("value1 == value2:");
        System.out.println(value1 == value2);
        System.out.println("value1 != value2:");
        System.out.println(value1 != value2);
        System.out.println("value1 > value2:");
        System.out.println(value1 > value2);
        System.out.println("value1 < value2:");
        System.out.println(value1 < value2);
        System.out.println("value1 <= value2");
        System.out.println(value1 <= value2);
    }
}
